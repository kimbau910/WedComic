const btnm = document.querySelector(".btn_banner");
const btns_main = document.querySelector(".main_mobile");
const iconn = document.querySelector(".icon_mobile")
btnm.onclick = function(){
  btns_main.classList.add("active_banner");
  btns_main.style.display ="block";
  if(iconn.onclick = function(){
    btns_main.classList.remove("active_banner");
  });
}





